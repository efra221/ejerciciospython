# Bucle While
contador = 0
while contador < 5:
    print(contador)
    contador += 1
    
# Bucle For
for i in range(5):
    print(i)
    
# Uso de Break
for i in range(10):
    if i == 5:
        break
    print(i)
    
# Uso de Continue
for i in range(5):
    if i == 2:
        continue
    print(i)

